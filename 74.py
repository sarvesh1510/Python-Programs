def get_squares(numbers):
    squares = [n**2 for n in numbers]  # List comprehension to get squares
    return squares

# List of numbers
nums = [1, 2, 3, 4]

# Call the function and store the result
squares_list = get_squares(nums)

# Print the list of squares
print(squares_list)
