# Function to add two numbers
def add(a, b):
    return a + b

# Function to subtract two numbers
def subtract(a, b):
    return a - b

# Main function to call the add and subtract functions
def main():
    result1 = add(10, 5)
    result2 = subtract(10, 5)
    print(f"10 + 5 = {result1}")
    print(f"10 - 5 = {result2}")

# This condition ensures that the main function runs only when the script is executed directly
if __name__ == "__main__":
    main()
